    echo "–§–∞–π–ª—ã —Å –∏–∑–º–µ–Ω–µ–Ω–∏—è–º–∏:"
    git status --short
    echo ""
    read -p "–ü—Ä–æ–¥–æ–ª–∂–∏—Ç—å? –õ–æ–∫–∞–ª—å–Ω—ã–µ –∏–∑–º–µ–Ω–µ–Ω–∏—è –±—É–¥—É—Ç —Å–æ—Ö—Ä–∞–Ω–µ–Ω—ã –≤ stash (y/n): " -n 1 -r
